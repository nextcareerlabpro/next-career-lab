  const W = 210; const sideW = 72;
  pdf.setFillColor(15, 118, 110); pdf.rect(0, 0, sideW, 297, "F");
  pdf.setFillColor(247, 254, 254); pdf.rect(sideW, 0, W - sideW, 297, "F");

  // Sidebar
  pdf.setTextColor(255, 255, 255); pdf.setFontSize(15); pdf.setFont("helvetica", "bold");
  const nameL = pdf.splitTextToSize(d.name || "Your Name", 60);
  pdf.text(nameL, 8, 16);
  let ly2 = 16 + nameL.length * 7;
  pdf.setFontSize(8); pdf.setFont("helvetica", "normal"); pdf.setTextColor(153, 246, 228);
  const roleL2 = pdf.splitTextToSize(d.role || "Title", 60); pdf.text(roleL2, 8, ly2); ly2 += roleL2.length * 5 + 5;

